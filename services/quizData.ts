export const getRandomQuestions = (categoryId: string, count: number): Question[] => {
  const category = quizData.find((c) => c.id === categoryId);
  if (!category) return [];

  const shuffled = [...category.questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
};

export const getDailyChallenge = (): Question[] => {
  const allQuestions = quizData.flatMap((c) => c.questions);
  const shuffled = [...allQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 5);
};

export const getQuestionsByDifficulty = (
  categoryId: string,
  difficulty: 'easy' | 'medium' | 'hard',
): Question[] => {
  const category = quizData.find((c) => c.id === categoryId);
  if (!category) return [];

  return category.questions.filter((q) => q.difficulty === difficulty);
};
